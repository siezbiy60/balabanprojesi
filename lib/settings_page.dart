  Future<void> _loadSettings() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      setState(() {
        _messageNotifications = prefs.getBool('messageNotifications') ?? true;
        _callNotifications = prefs.getBool('callNotifications') ?? true;
        _soundEnabled = prefs.getBool('soundEnabled') ?? true;
        _vibrationEnabled = prefs.getBool('vibrationEnabled') ?? true;
        _isLoading = false;
      });
    } catch (e) {
      print('‚ùå Ayarlar y√ºklenirken hata: $e');
      setState(() => _isLoading = false);
    }
  }

  Future<void> _saveNotificationSetting(String key, bool value) async {
    try {
      final prefs = await SharedPreferences.getInstance();
      await prefs.setBool(key, value);
      print('‚úÖ Bildirim ayarƒ± kaydedildi: $key = $value');
    } catch (e) {
      print('‚ùå Bildirim ayarƒ± kaydedilemedi: $e');
